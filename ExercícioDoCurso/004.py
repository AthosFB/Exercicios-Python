c = input('\033[33mDigite algo: ')
print()
print('\033[36mEle é um\033[m', type(c))
print('\033[36mÉ tudo maiusculo:\033[m', c.isupper())
print('\033[36mÉ tudo minusculo:\033[m', c.islower())
print('\033[36mEsta captalizada:\033[m',c.istitle())
print('\033[36mÉ Alfanumérico:\033[m', c.isalnum())
print('\033[36mSó tem letras:\033[m', c.isalpha())
print('\033[36mÉ número:\033[m', c.isnumeric())
print('\033[36mÉ composto somente por espaços:\033[m', c.isspace())
